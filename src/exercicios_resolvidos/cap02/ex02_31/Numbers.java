package exercicios_resolvidos.cap02.ex02_31;

// Exercise 2.31 Solution: Numbers.java
// Program prints a table of squares and cubes of numbers from 0 to 10.

public class Numbers 
{
   public static void main(String[] args)
   {
      // print a header for the table
      System.out.printf("%s\t%s\t%s%n", "number", "square", "cube");

      // print x, x squared and x cubed for each value
      int x = 0;
      System.out.printf("%d\t%d\t%d%n", x, (x * x), (x * x * x));
      x = 1;
      System.out.printf("%d\t%d\t%d%n", x, (x * x), (x * x * x));
      x = 2;
      System.out.printf("%d\t%d\t%d%n", x, (x * x), (x * x * x));
      x = 3;
      System.out.printf("%d\t%d\t%d%n", x, (x * x), (x * x * x));
      x = 4;
      System.out.printf("%d\t%d\t%d%n", x, (x * x), (x * x * x));
      x = 5;
      System.out.printf("%d\t%d\t%d%n", x, (x * x), (x * x * x));
      x = 6;
      System.out.printf("%d\t%d\t%d%n", x, (x * x), (x * x * x));
      x = 7;
      System.out.printf("%d\t%d\t%d%n", x, (x * x), (x * x * x));
      x = 8;
      System.out.printf("%d\t%d\t%d%n", x, (x * x), (x * x * x));
      x = 9;
      System.out.printf("%d\t%d\t%d%n", x, (x * x), (x * x * x));
      x = 10;
      System.out.printf("%d\t%d\t%d%n", x, (x * x), (x * x * x));
   } // end main
} // end class Numbers


