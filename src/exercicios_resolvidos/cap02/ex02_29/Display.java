package exercicios_resolvidos.cap02.ex02_29;

// Exercise 2.29 Solution: Display.java
// Program that prints unicode characters
// and their integer equivalents.

public class Display 
{
   public static void main(String[] args)
   {
      System.out.printf("The character %c has the value %d%n",
         'A', ((int) 'A'));
      System.out.printf("The character %c has the value %d%n",
         'B', ((int) 'B'));
      System.out.printf("The character %c has the value %d%n",
         'C', ((int) 'C'));
      System.out.printf("The character %c has the value %d%n",
         'a', ((int) 'a'));
      System.out.printf("The character %c has the value %d%n",
         'b', ((int) 'b'));
      System.out.printf("The character %c has the value %d%n",
         'c', ((int) 'c'));
      System.out.printf("The character %c has the value %d%n",
         '0', ((int) '0'));
      System.out.printf("The character %c has the value %d%n",
         '1', ((int) '1'));
      System.out.printf("The character %c has the value %d%n",
         '2', ((int) '2'));
      System.out.printf("The character %c has the value %d%n",
         '$', ((int) '$'));
      System.out.printf("The character %c has the value %d%n",
         '*', ((int) '*'));
      System.out.printf("The character %c has the value %d%n",
         '+', ((int) '+'));
      System.out.printf("The character %c has the value %d%n",
         '/', ((int) '/'));
      System.out.printf("The character %c has the value %d%n",
         ' ', ((int) ' '));
   } // end main
} // end class Display


