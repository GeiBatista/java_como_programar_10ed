package exercicios_resolvidos.cap12.ex12_19;

// Exercise 12.19 Solution: Ecofont.java
// Driver program for Ecofont display.
import javax.swing.JFrame;

public class Ecofont
{
   public static void main(String[] args)
   {
      EcofontFrame ecofontFrame = new EcofontFrame();
      ecofontFrame.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
      ecofontFrame.setSize(400, 200); // set frame size
      ecofontFrame.setVisible(true); // display frame
   } 
} // end class Ecofont


